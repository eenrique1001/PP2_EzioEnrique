            <section>
                <h4>O que é a Malária?</h4>
                <p>
                    A malária é uma doença infecciosa transmitida por mosquitos, causada por protozoários do gênero Plasmodium. 
                    Ela é prevalente em regiões tropicais e subtropicais, representando uma ameaça significativa para a saúde 
                    global. Os sintomas da malária incluem febre, calafrios, fadiga, dores de cabeça, dores musculares e, em 
                    casos graves, pode levar à anemia, insuficiência renal e até mesmo a morte.
                </p>
                <div class="imgetexto">
                    <img src="images/001.png" alt="ciclo da vida malaria">
